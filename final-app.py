# Prepare data for the map
country_data = df.groupby('Country')['Valuation'].sum().reset_index()
country_data.columns = ['Country', 'Total Valuation']

# Create the map visualization
fig_map = px.choropleth(
    country_data,
    locations='Country',
    locationmode='country names',
    color='Total Valuation',
    hover_name='Country',
    color_continuous_scale=px.colors.sequential.Plasma,
    title='Total Startup Valuations by Country',
    labels={'Total Valuation': 'Total Valuation (Billion USD)'},
)

fig_map.update_geos(
    visible=False,
    showcoastlines=True,
    showcountries=True,
)

fig_map.update_layout(
    template='plotly_white',
    height=500,
)

st.plotly_chart(fig_map, use_container_width=True)



