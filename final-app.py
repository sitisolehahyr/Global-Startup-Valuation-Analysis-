# Calculate average valuation by industry and sort
industry_avg = df.groupby('Industry')['Valuation'].mean().sort_values(ascending=True)
top_10_industries = industry_avg.tail(10)

col1, col2 = st.columns(2)

with col1:
    fig_industry = px.bar(
        x=top_10_industries.values,
        y=top_10_industries.index,
        orientation='h',
        title='Top 10 Industries by Average Valuation',
    )
    fig_industry.update_layout(
        xaxis_title="Average Valuation (Billion USD)",
        yaxis_title="Industry",
        template='plotly_white',
        height=500,
        showlegend=False
    )
    st.plotly_chart(fig_industry, use_container_width=True)

with col2:
    # Define df_top_industries based on the top 10 industries
    df_top_industries = df[df['Industry'].isin(top_10_industries.index)]

    # Scatter plot of valuation vs time with industry color coding
    fig_scatter = px.scatter(
        df_top_industries,
        x='Valuation_date',
        y='Valuation',
        color='Industry',
        title='Valuation Distribution Over Time by Industry'
    )
    fig_scatter.update_layout(
        xaxis_title="Date",
        yaxis_title="Valuation (Billion USD)",
        template='plotly_white',
        height=500
    )
    st.plotly_chart(fig_scatter, use_container_width=True)

